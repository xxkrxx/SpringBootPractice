package com.example.demo.form;

public class LoginForm {
    private String id;
    private String name;

    public LoginForm() {}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

